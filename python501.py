a = 4   # 0000 0000
b = 14  # 0000 1110
c = 25  # 0001 1001
d = 231 # 1110 0111
print(bin(a))
print(bin(b))
print(bin(c))
print(bin(d))
print(bin(a&b)) # 0000 0100 && 0000 1110 = 0000 0100
print(bin(a|b)) # 0000 0100 && 0000 1110 = 0000 1110
print(bin(a^b)) # 0000 0100 && 0000 1110 = 0000 1010
print(bin(~a))  # 0000 0100 && 0000 1110 = 0000 0100